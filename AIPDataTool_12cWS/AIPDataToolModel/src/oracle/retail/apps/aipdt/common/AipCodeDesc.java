package oracle.retail.apps.aipdt.common;

public class AipCodeDesc {
    public AipCodeDesc() {
        super();
    }
    
    private String code;
    private String desc;


    public void setCode(String code) {
        this.code = code;
    }

    public String getCode() {
        return code;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getDesc() {
        return desc;
    }
}
